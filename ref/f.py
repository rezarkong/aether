def sbox_lookup(x, sbox, b):
    return int2bits(sbox[bits2int(x)], b)

def branch(x, last):
    # substitution
    u = [0] * 128
    for i in range(16):
        j = 8 * i
        u[j : j + 3]     = sbox_lookup(x[j : j + 3], s3, 3)
        u[j + 3 : j + 8] = sbox_lookup(x[j + 3 : j + 8], s5, 5)

    if last:
        return u
   
    # theta
    v = [0] * 128
    for i in range(128):
        v[i] = u[(i + 12) % 128] ^ u[(i + 31) % 128] ^ u[(i + 86) % 128] 

    # pi
    t = [ v[(117 * i) % 128] for i in range(128) ]

    return t

def f_gleeok(x):
    for i in range(3):
        x = branch(x, x == 2)
    return x

def f_enae(x):
    z = bits2nibbles(x)
    
    # 1st s-box
    u = [0]*32
    for i in range(32):
        u[i] = s4[z[i]]

    # matrix mul
    v = [0]*32
    for i in range(16):
        for j in range(16):
            v[i]    ^= (m[i][j] * u[j])
            v[i+16] ^= (m[i][j] * u[j+16])

    # 2nd s-box
    w = [0]*32
    for i in range(32):
        w[i] = s4[v[i]]

    # nibble permutation
    t = [0]*32
    for i in range(32):
        t[p[i]] = w[i]

    return nibbles2bits(t)

